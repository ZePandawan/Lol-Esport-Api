const express = require('express');
const app = express();
const PORT = 6969;

app.use(express.json());

// #########################################################################################################################
// ##################################################### DEFAULT ROUTE #####################################################
// #########################################################################################################################
app.get('/', (req, res) => {
    res.json({
        name: 'LoL API',
        version: "0.1.0",
        author: "Vincent REIX <vincentreix54@gmail.com>",
        repository: ""
    });
});
// #########################################################################################################################



// #########################################################################################################################
// ##################################################### LEAGUES ROUTES ####################################################
// #########################################################################################################################
app.get('/leagues',(req,res) => {
  res.json({});
});

app.get('/leagues/id/:id',(req,res) => {
  const leagueId = req.params.id;
  res.json({
    id: leagueId
  });
});

app.get('/leagues/name/:name',(req,res) => {
  const leagueName = req.params.name;
  res.json({
    name: leagueName
  });
});
// #########################################################################################################################



// #########################################################################################################################
// ################################################### TOURNAMENTS ROUTES ##################################################
// #########################################################################################################################
app.get('/tournaments',(req,res) => {
  res.json({});
});

app.get('/tournaments/league/:id',(req,res) => {
  const leagueId = req.params.id;
  res.json({
    id: leagueId
  });
});

app.get('/tournaments/:id',(req,res) => {
  const tournamentId = req.params.id;
  res.json({
    name: tournamentId
  });
});
// #########################################################################################################################



// #########################################################################################################################
// ###################################################### TEAMS ROUTES #####################################################
// #########################################################################################################################
app.get('/teams',(req,res) => {
  res.json({});
});

app.get('/teams/:search',(req,res) => {
  const searchRequest = req.params.search;
  res.json({
    id: searchRequest
  });
});

app.get('/teams/league/:name',(req,res) => {
  const leagueName = req.params.name;
  res.json({
    name: leagueName
  });
});

app.get('/teams/region/:name',(req,res) => {
  const regionName = req.params.name;
  res.json({
    name: regionName
  });
});
// #########################################################################################################################



// #########################################################################################################################
// ###################################################### MATCHS ROUTE #####################################################
// #########################################################################################################################
app.get('/match/:id',(req,res) => {
  const matchId = req.params.id;
  res.json({
    name: matchId
  });
});
// #########################################################################################################################



// #########################################################################################################################
// #################################################### 404 ERROR ROUTE ####################################################
// #########################################################################################################################
app.use((req, res, next) => {
  res.status(404).send('Sorry, this route does not exist!');
});
// #########################################################################################################################

// Launch server on specified port
app.listen(PORT, () => {
    console.log(`Server running on port ${PORT}`);
});
